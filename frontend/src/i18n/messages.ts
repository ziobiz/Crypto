export const messages: Record<Locale, Dict> = { KR, JP, US, CH, TH };

export function translate(
  locale: Locale,
  key: MessageKey,
  vars?: Record<string, string | number>,
): string {
  const dict = messages[locale] ?? messages.KR;
  let text = dict[key] ?? messages.KR[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
    }
  }
  return text;
}
